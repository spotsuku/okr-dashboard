-- ─────────────────────────────────────────────
-- マネージャー会議のステップ番号を新仕様にシフトするマイグレーション
--
-- 旧 (5値):
--   0=未開始 / 1=チームサマリー / 2=確認事項 / 3=ネクストアクション / 4=終了
-- 新 (6値):
--   0=未開始 / 1=KR順送り / 2=チームサマリー / 3=横断連携・確認事項
--   / 4=ネクストアクション / 5=終了
--
-- 既存の manager セッションの step を +1 シフトする。
-- これにより:
--   旧 step=1 (チームサマリー) → 新 step=2 (チームサマリー) ✓
--   旧 step=4 (終了)           → 新 step=5 (終了)         ✓
--
-- 1度だけ実行。再実行で二重シフトを防ぐため、まだシフトされていない
-- (step <= 4 で finished_at が NULL ではない or 在りうる範囲) のみ対象にする。
-- ──────────────────────────────────────────────

-- 安全のため、既に新仕様 (step=5) にあるレコードは触らない。
-- 旧仕様 (step 1〜4) のみを +1 シフトする。
UPDATE weekly_mtg_sessions
SET step = step + 1
WHERE meeting_key = 'manager'
  AND step BETWEEN 1 AND 4;

NOTIFY pgrst, 'reload schema';

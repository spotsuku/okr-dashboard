// 'YYYY-MM-DD' を受け取り、土日 or 祝日なら true
export function isJpNonBusinessDay(dateStr) {
  if (JP_HOLIDAYS.has(dateStr)) return true
  const [y, mo, d] = dateStr.split('-').map(Number)
  const dow = new Date(y, mo - 1, d).getDay()  // 0=日, 6=土
  return dow === 0 || dow === 6
}
